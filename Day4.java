public class Day4 {
    public static void main(String[] args) {
        // \n untuk baris baru
        System.out.println("Ini baris pertama!\nIni baris kedua.");

        // \t untuk tab
        System.out.println("Athiyah\t18 tahun\tMajene");

        // \" untuk menampilkan tanda kutip ganda
        System.out.println("Halo: \"Selamat malam semuanya!\"");

        // \\ untuk menampilkan backslash
        System.out.println("Folder saya ada di: C:\\Users\\Athiyah\\Documents");
    }
}