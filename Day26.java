package days26;
public class Days26 {
    public static void main(String[] args) {
        int x = 8; //nilai awal
        int y = 3;

        x += y;
        System.out.println("Setelah x += y, hasil = " + x);

        x -= y;
        System.out.println("Setelah x -= y, hasil = " + x);

        x *= y;
        System.out.println("Setelah x *= y, hasil = " + x);

        x /= y;
        System.out.println("Setelah x /= y, hasil = " + x);

        x %= y;
        System.out.println("Setelah x %= y, hasil = " + x);

    }
}