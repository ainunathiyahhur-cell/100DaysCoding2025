public class day9 {
    public static void main(String[] args) {
        // Deklarasi variabel (menentukan tipe data dan nama variabel)
        int angka;
        String nama;

        // Inisialisasi variabel (memberi nilai awal)
        angka = 17;
        nama = "Athiyah";

        // Menampilkan nilai awal
        System.out.println("Nilai awal angka: " + angka);
        System.out.println("Nilai awal nama: " + nama);

        // Update variabel (mengubah nilai yang sudah ada)
        angka = 18;
        nama = "Hur Ainun";

        // Menampilkan nilai setelah diupdate
        System.out.println("Nilai setelah update angka: " + angka);
        System.out.println("Nilai setelah update nama: " + nama);
    }
}