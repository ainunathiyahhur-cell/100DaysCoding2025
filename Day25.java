public class Day25 {
    public static void main(String[] args) {
        //increment
        int x = 10;
      
        System.out.println("Nilai awal = " + x);
        x++;
        System.out.println("Setelah = " + x);
        
        //Decrement
        int a = 5;
        System.out.println("Nilai awal = " + a);
        a--;
        System.out.println("Setelah = " + a);
    }
    
}